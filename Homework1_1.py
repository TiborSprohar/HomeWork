mood = input("Kakšne volje si danes? ")

if mood == "Dobre":
    print("Lepo te je videti srečnega!")

elif mood == "Slabe":
    print("Trikrat zajemi sapo!")

elif mood == "Žalosten":
    print("Ne bodi žalosten")

elif mood == "Navdušen":
    print("Veselim se s teboj!!!")

elif mood == "Sproščen":
    print("To ti koristi")

else :
    print("Tega pa ne prepoznam!")

